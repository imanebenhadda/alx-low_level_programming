#include <stdio.h>
/**
 * add - addition function
 * @a: first number
 * @b: second number
 *
 * Return: a + b
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - substraction function
 * @a: first number
 * @b: second number
 *
 * Return: a - b
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * mul - multiplication function
 * @a: first number
 * @b: second number
 *
 * Return: a * b
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * div - divition function
 * @a: first number
 * @b: second number
 *
 * Return: a / b
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 * mod - modulo function
 * @a: first number
 * @b: second number
 *
 * Return: a % b
 */
int mod(int a, int b)
{
	return (a % b);
}
